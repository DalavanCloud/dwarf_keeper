using namespace std;

string cp850_to_utf8(string src){
    string dst;
    const char*c;
    int l=src.size();
    dst.reserve(l + l/10);
    const unsigned char*p = (const unsigned char*) src.data();

    for(int i=0;i<l;i++){
        dst += CP850_TO_UTF8[p[i]];
    }
    return dst;
}

